public class NewsModel {

    String icon;
    String titolo;
    String desc;

    public String getIcon() {
        return icon;
    }

    public String getTitolo() {
        return titolo;
    }

    public String getDesc() {
        return desc;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public void setTitolo(String titolo) {
        this.titolo = titolo;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
